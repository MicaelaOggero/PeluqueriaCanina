    private void cargarTabla() {

        DefaultTableModel tabla = new DefaultTableModel() {

            //para que filas y columnas no sean editables 
            @Override
            public boolean isCellEditable(int fila, int columna) {
                return false;
            }
        };

        //establecemos los nombres de las columnas
        String titulos[] =
        {
            "Num", "Nombre", "Color", "Raza", "Alergico", "At. Especial", "Due√±o", "Celular"
        };

        tabla.setColumnIdentifiers(titulos);

        //carga de los datos desde la BD
        List<Mascota> listaMascotas = control.traerMascotas();

        //recorrer la lista y mostrar cada uno de los elementos
        if (listaMascotas != null)
        {
            for (Mascota masco : listaMascotas)
            {
                Object[] objeto =
                {
                    masco.getNumCliente(), masco.getNombre(), masco.getColor(), masco.getRaza(), masco.getAlergico(), masco.getAtencionEspecial(), masco.getDuenio().getNombre(), masco.getDuenio().getCelular()
                };

                tabla.addRow(objeto);

            }
        }

        tblDatos.setModel(tabla);

    }

    private void mostrarMensaje(String mensaje, String tipoMensaje, String titulo) {

        JOptionPane optionPane = new JOptionPane(mensaje);
        if (tipoMensaje.equals("Info"))
        {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (tipoMensaje.equals("Error"))
        {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);

    }
