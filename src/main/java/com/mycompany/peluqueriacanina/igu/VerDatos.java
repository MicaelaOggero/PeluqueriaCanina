    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        if (tblDatos.getRowCount() > 0)
        {//cuenta la cantidad de filas 

            if (tblDatos.getSelectedRow() != -1)
            { //verifica hay una fila seleccionada

                //el cero indica el valor de la columna - trae el valor de la fila seleccionada y como las tablas guardan los datos en tipo String lo transforma
                int numCliente = Integer.parseInt(String.valueOf(tblDatos.getValueAt(tblDatos.getSelectedRow(), 0)));

                control.borrarMascota(numCliente);

                mostrarMensaje("Eliminado correctamente", "Info", "Borrar");

                cargarTabla();

            } else
            {
                mostrarMensaje("Ninguna fila seleccionada", "Error", "Error");
            }

        } else
        {
            mostrarMensaje("No hay datos para eliminar", "Error", "Error");
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

